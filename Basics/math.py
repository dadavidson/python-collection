#!/usr/bin/env python3

# Math
print(50 + 50)                  # Addition
print(50 - 50)                  # Subtraction
print(50 * 50)                  # Multiplication
print(50 / 50)                  # Division

print(50 + 50 - 50 * 50 / 50)   # PEMDAS
print(50 ** 2)                  # Exponents
print(50 % 6)                   # Modulus
print(50 / 6)                   # Float Division
print(50 // 6)                  # Flat Division
